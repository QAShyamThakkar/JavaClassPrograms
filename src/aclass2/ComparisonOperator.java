package aclass2;

public class ComparisonOperator {

	public static void main(String[] args) {
		
		
		int num = 10;
		
		System.out.println( num > 10 );
		System.out.println( num < 10 );
		System.out.println( num >= 10 );
		System.out.println( num <= 10 );
		
		System.out.println( num == 10 );  // true
		System.out.println(num != 10);   // false

	}

}
