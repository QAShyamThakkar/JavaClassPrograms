package fclass9;

public class Loop {

	public static void main(String[] args) {
		
		// print Hello Sprinters 10 times
		
		// any action that you want to repeat n times then you can use loop
		
		int i = 1;  //1. looping variable
		while(i <= 100) {    // 2. condition if true execute while loop
			System.out.println("Hello Sprinters");
			i++;          // 3. increment variable
		}
		
		
	}

}
