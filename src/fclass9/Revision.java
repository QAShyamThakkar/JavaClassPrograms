package fclass9;

public class Revision {

	public static void main(String[] args) {
		
		int a = 10;
		
		a++;   // increment the value by 1
		
		System.out.println("Value of a = "+a);
		
		
		System.out.println("************************");
		
		int x = 5;
		
		int y = ++x;
		System.out.println("Value of x ="+x);
		System.out.println("Value of y = "+y);
	}

}
