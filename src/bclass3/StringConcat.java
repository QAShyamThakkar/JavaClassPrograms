package bclass3;

public class StringConcat {
	
	public static void main(String args[]) {
		
		
		String str1 = "Hello";
		
		
		String str2 = "World";
		
		
		String str3 = str1 + str2;  // + is to concat/combine two strings
		
		System.out.println(str3);
		
		System.out.println(10+20);  //+ is to add two numbers
		
		System.out.println(10 + str1 + 20);  //10Hello20
		
		System.out.println(10 + 20 + str1);
		
		System.out.println(20-10);
		
		
	}
	

}
