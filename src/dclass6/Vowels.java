package dclass6;

public class Vowels {

	public static void main(String[] args) {
		
		// check if character is a vowel or consonant
		// vowels -> a,e,i,o,u
		
		char c = 'i';
		
		if( c == 'a' || c=='e' || c=='i' || c=='o' || c=='u') {
			System.out.println(c +" is a vowel");
		}
		else {
			System.out.println(c + " is a consonant");
		}
		

	}

}
