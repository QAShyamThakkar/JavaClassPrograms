package eclass7;

public class TypeCasting {

	public static void main(String[] args) {
		
		
		
		
		int a=10;     // small cup
		
		double d;     // large cup
		 d=a;         // implicit type casting
		 System.out.println(d);
		 
		 short s = 6;    // 16 bits
		 long l ;        // 64 bits
		 
		 l = s;     //    implicit type casting
		 
		 
		 
		 
	}

}
