package iclass12;

public class NestedForLoop {

	public static void main(String[] args) {
		
		for(int i=1; i<=3 ; i++) {  // outer for loop
			
			System.out.println("I am learning Java");
			
			for(int j=1; j<=2 ; j++) {  // inner for loop
				System.out.println("Hello");
			}
			
		}
		
		
		

	}

}
